/**
 * 构建剧本生成的 User Prompt
 */
export function buildScriptGenerateUserPrompt(params: {
	idea: string;
	genre?: string;
	tone?: string;
	targetDuration?: number;
	characterCount?: number;
}): string {
	const { idea, genre, tone, targetDuration, characterCount } = params;

	let prompt = `# 创意想法：${idea}\n\n# 偏好设置：`;

	if (genre) {
		prompt += `\n- 类型：${genre}`;
	}
	if (tone) {
		prompt += `\n- 基调：${tone}`;
	}
	if (targetDuration) {
		prompt += `\n- 预估时长：${targetDuration}分钟`;
	}
	if (characterCount) {
		prompt += `\n- 主要角色数：${characterCount}人`;
	}

	prompt += `\n---\n\n请创作一个符合以上要求的短剧剧本，按照指定的格式输出。`;

	return prompt;
}
